using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Oops
{
    class Polymorphism
    {
        // Multiple Forms
        public int add(int a, int b)
        {
            return a + b;
        }
        public int  add(int a,int b,int c)
        {
            return a + b + c;
        }
    }
}
